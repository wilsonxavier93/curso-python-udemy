# Aspas simples
print('Wilson Xavier') # Aspas simples são usadas para strings que não contenham aspas

# Aspas duplas
print("Wilson Xavier") # Aspas duplas são usadas para strings que contenham aspas

#Escape
print("Wilson  Xavier disse \"Olá, mundo!\"") # O caractere \ é usado para escapar o caractere que está após ele

# r
print(r"Wilson Xavier disse \"Olá, mundo!\"") # O caractere r é usado para indicar que a string é uma string raw, ou seja, não é interpretada como uma string com escape

# A  forma mais fácil é diferenciar as aspas
print('Wilson Xavier disse "Olá, mundo!"') # A forma mais fácil é usar as aspas simples para aspas duplas e vice-versa